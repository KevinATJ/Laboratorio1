#include<stdio.h>

int main(){
	
	float num1;
	float num2;
	
	printf("Prueba de cuatro operaciones:\n\n");
	printf("ingrese un valor para num1: ");
	scanf("%f",&num1);
	printf("ingrese un valor para num2: ");
	scanf("%f",&num2);
	printf("\n");
	printf("Operacion suma");
	printf("num1+num2 = %f",num1+num2);
	printf("\n");
	printf("Operacion resta");
	printf("\n");
	printf("num1-num2 = %f",num1-num2);
	printf("\n");
	printf("Operacion multiplicacion");
	printf("\n");
	printf("num1*num2 = %f",num1*num2);
	printf("\n");
	printf("Operacion division");
	printf("\n");
	printf("num1/num2 = %f",num1/num2);
	
	return 0;
}
